function applySorting(sortType) {
  const list = document.getElementById('list');
  const cards = Array.from(list.children);

  cards.sort((a, b) => {
    // D'abord trier par favoris
    const aIsFavorite = a.classList.contains('favorite');
    const bIsFavorite = b.classList.contains('favorite');
    
    if (aIsFavorite && !bIsFavorite) return -1;
    if (!aIsFavorite && bIsFavorite) return 1;
    
    // Ensuite appliquer le tri sp√©cifi√©
    if (sortType === 'alphabetical') {
      const nameA = a.dataset.name.toLowerCase();
      const nameB = b.dataset.name.toLowerCase();
      return nameA.localeCompare(nameB);
    } else { // chronological
      const dateA = a.dataset.date;
      const dateB = b.dataset.date;
      
      // R√©cup√©rer les heures depuis les donn√©es du groupe
      const indexA = parseInt(a.dataset.index, 10);
      const indexB = parseInt(b.dataset.index, 10);
      const timeA = data[indexA].start;
      const timeB = data[indexB].start;
      
      const dateCompare = dateA.localeCompare(dateB);
      if (dateCompare !== 0) return dateCompare;
      return timeA.localeCompare(timeB);
    }
  });

  cards.forEach(card => list.appendChild(card));
}

